-module(arizona_config).

%% --------------------------------------------------------------------
%% API function exports
%% --------------------------------------------------------------------

-export([endpoint/0]).

%% --------------------------------------------------------------------
%% API function definitions
%% --------------------------------------------------------------------

-spec endpoint() -> arizona_server:opts().
endpoint() ->
    get_env(endpoint, #{}).

%% --------------------------------------------------------------------
%% Private functions
%% --------------------------------------------------------------------

get_env(Key, Default) ->
    application:get_env(arizona, Key, Default).
